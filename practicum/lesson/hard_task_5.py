
"""
Задача сложная:
Написать программу которая должна оценить сразу несколько автомобилей, чтобы 
пользователь мог ввести информацию о нескольких автомобилях (использовать for)

Стоимость автомобиля зависит от его данных:
Марка, год выпуска, пробег, состояние(новый/бу)

Коэффициенты произвольные.
Базовая стоимость авто = 20000
"""

base_price = 20000

num_cars = int(input("Введите количество автомобилей для оценки: "))

for i in range(num_cars):
    print(f"Введите информацию об автомобиле {i + 1}")
    
    brand = input("Марка автомобиля: ")
    year = int(input("Год выпуска: "))
    mileage = int(input("Пробег: "))
    condition = input("Состояние (новый/бу): ")
    
    price = base_price
    if year < 2020:
        price -= 2000  # Старые автомобили дешевле на 2000
    if mileage > 100000:
        price -= 3000  # Автомобили с большим пробегом дешевле на 3000
    if condition == "бу":
        price -= 5000  # Б/у автомобили дешевле на 5000

    print(f"Стоимость автомобиля составляет: {price}")

